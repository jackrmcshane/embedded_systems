  RingBuffer_Init(&ring); // init ringbuffer
  uint8_t buffer[BUFFER_LENGTH];
  uint8_t *buff = buffer;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

